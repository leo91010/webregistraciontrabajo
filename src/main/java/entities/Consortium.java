package entities;

public class Consortium {
	
	public String address;
	
	public String nameInC;
	
	public String lastNameInC;
	
	public String cellphoneInC;
	
	public String cellphoneConsortiumRefferent;
	
	
	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getNameInC() {
		return nameInC;
	}

	public void setNameInC(String nameInC) {
		this.nameInC = nameInC;
	}

	public String getLastNameInC() {
		return lastNameInC;
	}

	public void setLastNameInC(String lastNameInC) {
		this.lastNameInC = lastNameInC;
	}

	public String getCellphoneInC() {
		return cellphoneInC;
	}

	public void setCellphoneInC(String cellphoneInC) {
		this.cellphoneInC = cellphoneInC;
	}

	public String getCellphoneConsortiumRefferent() {
		return cellphoneConsortiumRefferent;
	}

	public void setCellphoneConsortiumRefferent(String cellphoneConsortiumRefferent) {
		this.cellphoneConsortiumRefferent = cellphoneConsortiumRefferent;
	}



}
